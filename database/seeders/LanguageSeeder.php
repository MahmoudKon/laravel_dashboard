    protected function insertRows($languages)
    {
        foreach ($languages as $language) {
            Language::firstOrCreate($language, 
                        array_merge($language,[
                            'icon' => $this->getFlag( $language['name'] ),
                            'active' => isset( $this->active_langs[ $language['short_name'] ] )
                        ]
                    ));
        }
    }

    protected function getFlag($full_name)
    {
        foreach ($this->countries as $key => $name) {
            if ( stripos( $full_name, $name ) !== false )
                return 'flag-icon flag-icon-'. strtolower($key);
        }
        return 'flag-icon flag-icon-eg';
    }
